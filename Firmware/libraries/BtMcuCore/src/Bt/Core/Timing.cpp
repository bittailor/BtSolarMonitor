#include "Bt/Core/Timing.hpp"

#include <Arduino.h>

namespace Bt {
namespace Core {


//-------------------------------------------------------------------------------------------------

void delayInMilliseconds(uint32_t milliseconds) {
   delay(milliseconds);
}

//-------------------------------------------------------------------------------------------------

void delayInMicroseconds(uint32_t microseconds) {
   delayMicroseconds(microseconds);
}

//-------------------------------------------------------------------------------------------------

uint32_t milliseconds() {
   return ::millis();
}

//-------------------------------------------------------------------------------------------------

uint32_t microseconds() {
   return ::micros();
}

//-------------------------------------------------------------------------------------------------

} // namespace Core
} // namespace Bt
